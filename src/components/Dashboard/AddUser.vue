  methods: {
    isEmailValid(email) {
      let email_regex = /^[a-z0-9._%+-]+@[a-z0-9.-]+\.[a-z]{2,4}$/g;
      if (email != '' && email != null && email != undefined) {
        email = email.toLowerCase();
        email = email.trim();
        return email_regex.test(email)
      }
    },
    isPhoneValid(value) {
      let mobile_regex = /^[6-9]\d{9}$/g;
      return mobile_regex.test(value)
    },
